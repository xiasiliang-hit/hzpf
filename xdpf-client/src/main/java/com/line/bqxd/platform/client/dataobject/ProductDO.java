package com.line.bqxd.platform.client.dataobject;


import com.line.bqxd.platform.client.common.DBBaseDO;

import java.io.Serializable;

/**
 * Created by huangjianfei on 16/4/28.
 */
public class ProductDO extends DBBaseDO implements Serializable{


    private static final long serialVersionUID = -2897989811804625218L;
    private String name;

    private String simpleDes;

    private String detail;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSimpleDes() {
        return simpleDes;
    }

    public void setSimpleDes(String simpleDes) {
        this.simpleDes = simpleDes;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
